package me.kuuds.codec.example.modbus;

import io.netty.channel.ChannelDuplexHandler;
import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.ChannelPromise;
import io.netty.channel.socket.DuplexChannel;

public class ModbusTcpTransportHandler extends ChannelDuplexHandler {

    private final DeviceSessionCtx deviceSessionCtx;

    public ModbusTcpTransportHandler(DeviceSessionCtx deviceSessionCtx) {
        this.deviceSessionCtx = deviceSessionCtx;
    }

    @Override
    public void channelActive(ChannelHandlerContext ctx) throws Exception {
        super.channelActive(ctx);
    }

    @Override
    public void channelInactive(ChannelHandlerContext ctx) throws Exception {
        super.channelInactive(ctx);
    }

    @Override
    public void read(ChannelHandlerContext ctx) throws Exception {
        super.read(ctx);
    }

    @Override
    public void write(ChannelHandlerContext ctx, Object msg, ChannelPromise promise) throws Exception {
        super.write(ctx, msg, promise);
    }
}
